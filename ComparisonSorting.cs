        /*
         * Complete the 'countingSort' function below.
         * 
         * see https://www.hackerrank.com/challenges/one-month-preparation-kit-countingsort1/problem?isFullScreen=true&h_l=interview&playlist_slugs%5B%5D=preparation-kits&playlist_slugs%5B%5D=one-month-preparation-kit&playlist_slugs%5B%5D=one-month-week-one
         *
         * The function is expected to return an INTEGER_ARRAY.
         * The function accepts INTEGER_ARRAY arr as parameter.
         */

        public static List<int> countingSort(List<int> arr)
        {
            var frequency = Enumerable.Repeat(0, 100).ToList();

            foreach (var item in arr)
            {
                frequency[item]++;
            }

            return frequency.Select(x => x).ToList();
        }
    }

